public class ForLoopDemo {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        for(int i =1; i <= n; i++){
            System.out.println(i);
        }
        for(int i =10; i >= 1; i--){
            System.out.println(i);}
    int sum = (n*(n+1)/2);
System.out .println("Sum is"+ sum);}
}
